function sumValues(val1, val2) {
  return val1 + val2;
}

function multiplyValues(val1, val2) {
  return val1 * val2;
}

function substractValues(val1, val2) {
  return val1 - val2;
}

function divideValues(val1, val2) {
  return val1 / val2;
}

const value1 = 8;
const value2 = 4;

console.log("Sum: " + sumValues(value1, value2));
console.log("Multiply: " + multiplyValues(value1, value2));
console.log("Substract: " + substractValues(value1, value2));
console.log("Divide: " + divideValues(value1, value2));
